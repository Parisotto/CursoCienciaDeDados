# VARIÁVEIS E ATRIBUIÇÃO:

# Em programação, variáveis são utilizadas para armazenar valores e para dar nome a uma área de 
# memória do computador onde armazenamos dados.

a = 1           # Leia 'a recebe 1'. A variável chamada 'a' receberá o valor '1'
b = 2           # Leia 'b recebe 2'. A variável chamada 'b' receberá o valor '2'
print(a + b)    # a função print(a + b) exibe o resultado da soma do conteúdo da 
                # variável a com o conteúdo da variável b.
                # No terminal, a função print() realiza a exibição na tela da adição de a + b: 3

# OUTROS EXEMPLOS:

variavel = 20
a = 30
texto = 'Essa é uma frase'
salário = 1500
salario = 750
aumento = True
média = 7.5 # Para representar o sete e meio usamos a pontuação "." e não vírgula ","
soma = 7,5 # Isso não é sete e meio e sim uma tupla com o valor 7 e 5. Veremos mais adiante no curso
a = 10 # a variável 'a' tinha o valor de 30 mas foi substituída aqui por 10

# Esses são os valores armazenados em cada uma das variáveis:

print(variavel) # 20
print(a) # 10
print(texto) # Essa é a saída
print(salário) # 1500
print(salario) # 750
print(aumento) # True
print(média) # 7.5
print(soma) # (7, 5)

